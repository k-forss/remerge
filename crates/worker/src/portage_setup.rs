//! Apply the portage configuration from a workorder to the worker container.
//!
//! This writes the client's USE flags, `make.conf` settings, `package.use`, etc.
//! into the container's `/etc/portage/` so that emerge produces packages
//! matching the requester's configuration.
//!
//! When cross-compiling, `CHOST` is set to the target and `CBUILD` to the
//! worker's native tuple so that portage knows it's a cross-build.

use std::path::Path;

use anyhow::{Context, Result};
use tokio::fs;
use tracing::info;

use remerge_types::portage::PortageConfig;

/// Apply the full portage configuration to this container.
///
/// `worker_chost` is the build machine's native CHOST (i.e. `CBUILD`).
/// When it differs from the target `CHOST` in `config.make_conf.chost`,
/// we're cross-compiling and need to set both `CHOST` and `CBUILD`.
pub async fn apply_config(
    config: &PortageConfig,
    worker_chost: &str,
    gpg_key: Option<&str>,
    gpg_home: Option<&str>,
) -> Result<()> {
    write_make_conf(config, worker_chost, gpg_key, gpg_home).await?;
    write_package_use(config).await?;
    write_package_accept_keywords(config).await?;
    write_package_license(config).await?;
    write_package_mask(config).await?;
    write_package_unmask(config).await?;
    write_package_env(config).await?;
    write_env_files(config).await?;
    write_repos_conf(config).await?;
    write_profile_overlay(config).await?;
    write_patches(config).await?;
    set_profile(config).await?;
    info!("Portage configuration applied");
    Ok(())
}

/// Testable inner variant of [`apply_config`] that writes all portage
/// configuration files relative to `base` (e.g. a temp dir) instead of
/// hardcoded `/etc/portage/`.
///
/// This does NOT call `ensure_repo_locations` or `set_profile`'s
/// on-disk repos.conf scanning, since those operate on absolute
/// container paths.  Profile is set based on repo locations derived
/// from the config's repos_conf.
pub async fn apply_config_inner(
    base: &Path,
    config: &PortageConfig,
    worker_chost: &str,
    gpg_key: Option<&str>,
    gpg_home: Option<&str>,
) -> Result<()> {
    write_make_conf_inner(base, config, worker_chost, gpg_key, gpg_home).await?;
    write_package_use_inner(base, config).await?;
    write_package_accept_keywords_inner(base, config).await?;
    write_package_license_inner(base, config).await?;
    write_package_mask_inner(base, config).await?;
    write_package_unmask_inner(base, config).await?;
    write_package_env_inner(base, config).await?;
    write_env_files_inner(base, config).await?;
    write_repos_conf_inner(base, config).await?;
    write_profile_overlay_inner(&base.join("profile"), config).await?;
    write_patches_inner(&base.join("patches"), config).await?;

    // Derive repo locations from the config for profile setting.
    let repo_locations: Vec<String> = config
        .repos_conf
        .values()
        .flat_map(|content| parse_repo_sections(content))
        .map(|(_, location)| location)
        .collect();

    set_profile_inner(&config.profile, &repo_locations, &base.join("make.profile")).await?;

    info!(
        "Portage configuration applied to {}",
        base.display()
    );
    Ok(())
}

/// Write/overwrite `/etc/portage/make.conf` with the client's settings.
///
/// Sets `CHOST` to the target and, when cross-compiling, also `CBUILD`.
async fn write_make_conf(
    config: &PortageConfig,
    worker_chost: &str,
    gpg_key: Option<&str>,
    gpg_home: Option<&str>,
) -> Result<()> {
    write_make_conf_inner(
        Path::new("/etc/portage"),
        config,
        worker_chost,
        gpg_key,
        gpg_home,
    )
    .await
}

/// Testable inner variant of [`write_make_conf`] that writes relative to `base`.
pub async fn write_make_conf_inner(
    base: &Path,
    config: &PortageConfig,
    worker_chost: &str,
    gpg_key: Option<&str>,
    gpg_home: Option<&str>,
) -> Result<()> {
    let mc = &config.make_conf;
    let mut content = String::new();

    content.push_str("# Generated by remerge-worker — do not edit.\n\n");

    // ── Target identity ──────────────────────────────────────────────
    content.push_str(&format!("CHOST=\"{}\"\n", mc.chost));

    // If the worker arch differs from the target, set CBUILD so portage
    // knows we're cross-compiling.
    let target_arch = mc.chost.split('-').next().unwrap_or("");
    let worker_arch = worker_chost.split('-').next().unwrap_or("");
    if target_arch != worker_arch {
        content.push_str(&format!("CBUILD=\"{worker_chost}\"\n"));
        info!(
            chost = %mc.chost,
            cbuild = %worker_chost,
            "Cross-compilation detected — setting CHOST and CBUILD"
        );
    }
    content.push('\n');

    // ── Compiler flags (already resolved — no `-march=native`) ───────
    content.push_str(&format!("CFLAGS=\"{}\"\n", mc.cflags));
    content.push_str(&format!("CXXFLAGS=\"{}\"\n", mc.cxxflags));
    content.push_str(&format!("LDFLAGS=\"{}\"\n", mc.ldflags));

    // MAKEOPTS: prefer server-provided parallelism settings (based on the
    // server hardware) over the client's value.  Per-package overrides in
    // package.env are still respected by portage.
    let makeopts = build_makeopts(&mc.makeopts);
    content.push_str(&format!("MAKEOPTS=\"{makeopts}\"\n"));

    if !mc.use_flags.is_empty() {
        if mc.use_flags_resolved {
            // USE flags were fully resolved on the client (profile defaults +
            // make.conf merged via portageq).  Prefix with -* to clear the
            // worker container's own profile defaults — we want EXACTLY the
            // client's resolved set, nothing more.
            content.push_str(&format!("USE=\"-* {}\"\n", mc.use_flags.join(" ")));
        } else {
            // Legacy client: only make.conf USE flags, no profile defaults.
            // Write as-is and hope the worker profile is close enough.
            content.push_str(&format!("USE=\"{}\"\n", mc.use_flags.join(" ")));
        }
    }

    content.push_str(&format!("ACCEPT_LICENSE=\"{}\"\n", mc.accept_license));
    content.push_str(&format!("ACCEPT_KEYWORDS=\"{}\"\n", mc.accept_keywords));

    {
        let mut features = if !mc.features.is_empty() {
            let mut f = mc.features.clone();
            if !f.iter().any(|feat| feat == "buildpkg") {
                f.push("buildpkg".into());
            }
            f
        } else {
            vec!["buildpkg".into(), "noclean".into()]
        };

        // Enable GPG signing if configured by the server.
        if gpg_key.is_some() && gpg_home.is_some() {
            for feat in ["binpkg-signing", "gpg-keepalive"] {
                if !features.iter().any(|f| f == feat) {
                    features.push(feat.into());
                }
            }
        }

        // Strip features that are ineffective in ephemeral containers.
        // ccache: no persistent cache directory mounted.
        // distcc: no distcc daemon configured in the container.
        let before = features.len();
        features.retain(|f| match f.as_str() {
            "ccache" | "distcc" => {
                tracing::warn!(
                    feature = %f,
                    "Stripping FEATURE — not available in ephemeral worker containers"
                );
                false
            }
            _ => true,
        });
        if features.len() < before {
            info!(
                stripped = before - features.len(),
                "Stripped container-incompatible FEATURES"
            );
        }

        content.push_str(&format!("FEATURES=\"{}\"\n", features.join(" ")));
    }

    if !mc.emerge_default_opts.is_empty() {
        content.push_str(&format!(
            "EMERGE_DEFAULT_OPTS=\"{}\"\n",
            mc.emerge_default_opts
        ));
    }

    // ── CPU capability flags (from cpuid2cpuflags) ───────────────────
    if let Some((ref var, ref flags)) = mc.cpu_flags {
        content.push_str(&format!("{var}=\"{}\"\n", flags.join(" ")));
    }

    // ── USE_EXPAND variables ─────────────────────────────────────────
    for (key, values) in &mc.use_expand {
        content.push_str(&format!("{key}=\"{}\"\n", values.join(" ")));
    }

    // ── Extra variables ──────────────────────────────────────────────
    for (key, value) in &mc.extra {
        // Validate key: must be a valid shell variable name
        // (uppercase letters, digits, underscores; must not start with digit).
        if key.is_empty()
            || !key.chars().all(|c| c.is_ascii_alphanumeric() || c == '_')
            || key.starts_with(|c: char| c.is_ascii_digit())
        {
            tracing::warn!(
                key,
                "Skipping extra make.conf variable with invalid key name"
            );
            continue;
        }

        // Reject values containing shell metacharacters that could be
        // interpreted when make.conf is sourced by bash.
        if value.contains(['`', '$', '\\', '"', '\n', '\r', '\0']) {
            tracing::warn!(key, "Skipping extra make.conf variable with unsafe value");
            continue;
        }
        content.push_str(&format!("{key}=\"{value}\"\n"));
    }

    // Always set PKGDIR so packages go to the mounted volume.
    content.push_str("\n# remerge: output directory for binpkgs\n");
    content.push_str("PKGDIR=\"/var/cache/binpkgs\"\n");

    // ── Binary package OpenPGP signing ────────────────────────────────
    if let (Some(key), Some(home)) = (gpg_key, gpg_home) {
        content.push_str("\n# Binary package OpenPGP signing (injected by remerge-server)\n");
        content.push_str("BINPKG_FORMAT=\"gpkg\"\n");
        content.push_str(&format!("BINPKG_GPG_SIGNING_KEY=\"{key}\"\n"));
        content.push_str(&format!("BINPKG_GPG_SIGNING_GPG_HOME=\"{home}\"\n"));
    }

    let make_conf_path = base.join("make.conf");
    fs::write(&make_conf_path, &content)
        .await
        .context("Failed to write make.conf")?;

    info!("Wrote {}", make_conf_path.display());
    Ok(())
}

/// Write `/etc/portage/package.use/remerge`.
